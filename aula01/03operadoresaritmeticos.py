# Obter o primeiro número
print('Informe um número')
numero1 = int(input())

# Obter o segundo número
print('Informe outro número')
numero2 = int(input())

# Cálculos
print('A soma dos valores é '          + str(numero1+numero2))
print('A subtração dos valores é '     + str(numero1-numero2))
print('A multiplicação dos valores é ' + str(numero1*numero2))
print('A divisão dos valores é '       + str(numero1/numero2))