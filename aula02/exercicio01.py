# 1ª Peça três notas e retorne a média.

# Obter notas
print('Informe a primeira nota')
nota1 = float(input())

print('Informe a segunda nota')
nota2 = float(input())

print('Informe a terceira nota')
nota3 = float(input())

# Realizar a média
media = (nota1 + nota2 + nota3) / 3

# Exibindo a média
print('A média obtida é ' + str(media))